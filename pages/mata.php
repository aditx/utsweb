<h1>Spesialis Mata</h1>

<center><p><img src="img/mata.jpg" width="350px" height="250px"/></p></center>
<p>Bagi siapa saja yang belum memiliki masalah dengan penglihatan, memeriksakan diri ke dokter mata merupakan cara preventif yang layak dilakukan. Hal ini merupakan cara yang efektif dalam mendeteksi masalah mata sebelum munculnya gejala. Selain itu, konsultasi dengan dokter memungkinkan pula terdeteksinya masalah kesehatan lainnya yang bisa berdampak kepada mata, semisal diabetes.</p>
