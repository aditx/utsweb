<h1>Spesialis Jantung</h1>

<center><p><img src="img/jantung.jpg" width="450px" height="250px"/></p></center>
<p>Ilmu penyakit jantung atau kardiologi adalah suatu cabang kedokteran yang berhubungan dengan studi dan perawatan kelainan-kelainan di sistem kardiovaskular, yaitu jantung, pembuluh darah, dan pembuluh nadi.

Kardiologi seringkali dipandang sebagai sub-spesialisasi dalam bidang kesehatan organ tubuh bagian dalam. Cabang ini memusatkan perhatian terhadap diagnosa dan perawatan kondisi kardiovaskular mulai dari kelainan yang bersifat bawaan hingga penyakit-penyakit jantung termasuk gagal jantung dan penyakit jantung koroner.</p>
