<h1>Spesialis Hati</h1>

<center><p><img src="img/hati.jpg" width="450px" height="250px"/></p></center>
<p>Hepatologi merupakan cabang ilmu kedokteran yang menangani diagnosis dan pengobatan penyakit yang menyerang hati, kantung empedu, pankreas, dan saluran empedu. Dokter yang menangani hepatologi disebut sebagai spesialis hepatologi.</p>
